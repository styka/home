// Bakes the post-audit "changes summary" mini-book (content/audyt-podsumowanie/
// manifest.json + per-chapter Markdown) into a generated TS module for the admin
// reader at /admin/audyt-podsumowanie. Mirrors scripts/copy-audyt.js but for the
// separate two-chapter summary ("Co zostało wykonane" / "Co pozostało na przyszłość").
//
// Source of truth: content/audyt-podsumowanie/*. Run as an early step of `npm run
// build`; the generated module is committed so `npm run dev` works on a fresh clone.
const fs = require("fs")
const path = require("path")

const ROOT = path.resolve(__dirname, "..")
const CONTENT_DIR = path.join(ROOT, "content", "audyt-podsumowanie")
const MANIFEST_PATH = path.join(CONTENT_DIR, "manifest.json")

function readChapter(slug) {
  const abs = path.join(CONTENT_DIR, `${slug}.md`)
  try {
    const content = fs.readFileSync(abs, "utf8")
    const stat = fs.statSync(abs)
    return { content, mtime: stat.mtime.toISOString() }
  } catch {
    return null
  }
}

const manifest = JSON.parse(fs.readFileSync(MANIFEST_PATH, "utf8"))

const chapters = manifest.chapters.map((c) => {
  const found = readChapter(c.slug)
  const markdown = found ? found.content : ""
  const hasContent = markdown.trim().length > 40
  const status = hasContent ? (c.draft ? "draft" : "done") : "planned"
  return {
    slug: c.slug,
    number: c.number ?? "",
    part: c.part ?? "",
    title: c.title,
    summary: c.summary ?? "",
    status,
    markdown,
    words: markdown.trim() ? markdown.trim().split(/\s+/).length : 0,
    updatedAt: found ? found.mtime : null,
  }
})

const banner = `// AUTO-GENERATED by scripts/copy-audyt-podsumowanie.js — DO NOT EDIT BY HAND.
// Regenerated on every \`npm run build\`. Holds the post-audit changes summary
// mini-book for the admin reader at /admin/audyt-podsumowanie.
// Edit content/audyt-podsumowanie/*.md instead.
`

const meta = {
  title: manifest.title ?? "Podsumowanie zmian",
  subtitle: manifest.subtitle ?? "",
  auditDate: manifest.auditDate ?? "",
}

const body =
  banner +
  `import type { AudytChapter } from "@/generated/audyt-book"\n\n` +
  `export const AUDYT_PODSUMOWANIE_META = ${JSON.stringify(meta, null, 2)}\n\n` +
  `export const AUDYT_PODSUMOWANIE_CHAPTERS: AudytChapter[] = ${JSON.stringify(chapters, null, 2)}\n\n` +
  `export const AUDYT_PODSUMOWANIE_GENERATED_AT = ${JSON.stringify(new Date().toISOString())}\n`

const outDir = path.join(ROOT, "src", "generated")
fs.mkdirSync(outDir, { recursive: true })
fs.writeFileSync(path.join(outDir, "audyt-podsumowanie-book.ts"), body, "utf8")

const done = chapters.filter((c) => c.status === "done").length
console.log(
  `✓ copy-audyt-podsumowanie: ${chapters.length} rozdziałów (done ${done}) → src/generated/audyt-podsumowanie-book.ts`
)
