// Bakes the spec-driven pipeline docs (repo-root .claude/spec-pipeline/*.md) into a
// generated TypeScript module so the admin panel (/admin/spec-pipeline) can display
// their *latest* version at runtime — without shipping them as report migrations and
// without relying on filesystem access in the deployed (standalone) Next.js server.
//
// Same idea and shape as scripts/copy-docs.js. Runs early in `npm run build`; the
// generated module is committed so `npm run dev` works on a fresh clone.
const fs = require("fs")
const path = require("path")

// We run from worldofmag/, so the repo root is one level up; .claude lives there.
const DOCS = [
  {
    key: "guide",
    title: "Przewodnik SDD",
    description: "Jak działa spec-driven pipeline i jak z niego korzystać.",
    candidates: ["../.claude/spec-pipeline/README.md"],
  },
  {
    key: "constitution",
    title: "Konstytucja (C-NN)",
    description: "Twarde, nienegocjowalne reguły inżynierskie egzekwowane przez pipeline.",
    candidates: ["../.claude/spec-pipeline/constitution.md"],
  },
]

function readFirst(candidates) {
  for (const rel of candidates) {
    const abs = path.resolve(__dirname, "..", rel)
    try {
      const content = fs.readFileSync(abs, "utf8")
      const stat = fs.statSync(abs)
      return { content, mtime: stat.mtime.toISOString() }
    } catch {
      // try next candidate
    }
  }
  return null
}

const docs = DOCS.map((d) => {
  const found = readFirst(d.candidates)
  const content = found
    ? found.content
    : `# ${d.title}\n\n_Plik nie został znaleziony podczas builda._`
  return {
    key: d.key,
    title: d.title,
    description: d.description,
    content,
    lines: content.split("\n").length,
    bytes: Buffer.byteLength(content, "utf8"),
    updatedAt: found ? found.mtime : null,
    found: !!found,
  }
})

const banner = `// AUTO-GENERATED by scripts/copy-spec-pipeline.js — DO NOT EDIT BY HAND.
// Regenerated on every \`npm run build\`. Holds the latest snapshot of the
// spec-driven pipeline docs for the admin guide (/admin/spec-pipeline).
`

const body =
  banner +
  `import type { AdminDoc } from "./admin-docs"\n\n` +
  `export const SPEC_PIPELINE_DOCS: AdminDoc[] = ${JSON.stringify(docs, null, 2)}\n\n` +
  `export const SPEC_PIPELINE_GENERATED_AT = ${JSON.stringify(new Date().toISOString())}\n`

const outDir = path.resolve(__dirname, "..", "src", "generated")
fs.mkdirSync(outDir, { recursive: true })
const outFile = path.join(outDir, "spec-pipeline.ts")
fs.writeFileSync(outFile, body, "utf8")

console.log(
  `✓ copy-spec-pipeline: ${docs.filter((d) => d.found).length}/${docs.length} docs baked into src/generated/spec-pipeline.ts`
)
for (const d of docs) {
  console.log(`  ${d.found ? "•" : "✗"} ${d.title} — ${d.lines} lines, ${d.bytes} B`)
}
